# Day 03 - Variables and Data Types

# Variables
# A variable is used to store data
name = "Sandeep"
age = 22
is_student = True

print(name)
print(age)
print(is_student)

# 1. Numbers (int, float)
count = 10          # integer
price = 99.50       # float

print(count)
print(price)

# 2. String
message = "Hello Python"
print(message)

# 3. Boolean
is_active = False
print(is_active)

# 4. List (ordered, changeable)
fruits = ["apple", "banana", "mango"]
print(fruits)

# 5. Tuple (ordered, unchangeable)
colors = ("red", "green", "blue")
print(colors)

# 6. Set (unordered, unique values)
numbers = {1, 2, 3, 3}
print(numbers)

# 7. Dictionary (key-value pair)
student = {
    "name": "Sandeep",
    "age": 22,
    "course": "Python"
}
print(student)
