# Day 02 - Basic Syntax in Python

# 1. Printing
print("Hello, Python!")  # Prints text to the screen

# 2. Comments
# This is a single-line comment
"""
This is a
multi-line comment
"""

# 3. Keywords
# Keywords are reserved words in Python. Examples:
# if, else, for, while, def, class, import, etc.

# 4. Identifiers
# Identifiers are names for variables, functions, classes, etc.
my_variable = 10  # valid identifier
# 2variable = 5   # invalid identifier (cannot start with a number)

# 5. Indentation
# Python uses indentation (spaces) to define code blocks
if my_variable > 5:
    print("Greater than 5")  # indented code belongs to the 'if' block
